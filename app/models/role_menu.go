package models

import "github.com/beego/beego/v2/client/orm"

type RoleMenu struct {
	Id     int `orm:"column(id);auto" description:"主键ID"`
	RoleId int `orm:"column(role_id)" description:"角色ID"`
	MenuId int `orm:"column(menu_id)" description:"菜单ID"`
}

func (t *RoleMenu) TableName() string {
	return "sys_role_menu"
}

func init() {
	orm.RegisterModel(new(RoleMenu))
}

// 插入数据
func (t *RoleMenu) Insert() (int64, error) {
	id, err := orm.NewOrm().Insert(t)
	if err != nil {
		return 0, err
	}
	return id, nil
}
